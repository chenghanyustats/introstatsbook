

## ----binomial_r_1------------------------------------------------------------------------------
## 1. P(X = 6)
dbinom(x = 6, size = 15, prob = 0.2) 
## 2. P(X >= 6) = 1 - P(X <= 5)
1 - pbinom(q = 5, size = 15, prob = 0.2) 


## ----binomial_r_2------------------------------------------------------------------------------
## 2. P(X >= 6) = P(X > 5)
pbinom(q = 5, size = 15, prob = 0.2, 
       lower.tail = FALSE)  
## 3. P(X = 0)
dbinom(x = 0, size = 15, prob = 0.2) 


## ----binomial_plot, echo = -1, out.width="67%"-------------------------------------------------
par(mar = c(4, 4, 2, 0), mgp = c(2.7, 1, 0), las = 1)
plot(x = 0:15, y = dbinom(0:15, size = 15, prob = 0.2), type = 'h', xlab = "x", 
     ylab = "P(X = x)", lwd = 5, main = "Binomial(15, 0.2)")


## ----poisson_r_1-------------------------------------------------------------------------------
(mu <- 4200 / 365)
## P(X = 10)
dpois(x = 10, lambda = mu)  


## ----poisson_r_2-------------------------------------------------------------------------------
## P(X > 10) = 1 - P(X <= 10)
1 - ppois(q = 10, lambda = mu)  
## P(X > 10)
ppois(q = 10, lambda = mu, 
      lower.tail = FALSE) 


## ----poisson_plot, out.width="60%"-------------------------------------------------------------
plot(0:24, dpois(0:24, lambda = mu), type = 'h', lwd = 5, 
     ylab = "P(X = x)", xlab = "x", main = "Poisson(11.5069)")



